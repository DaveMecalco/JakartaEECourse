package Section9_ClaseMath;

public class EjemploFuncionesTrigonometricas {
    public static void main(String[] args) {

        //Función seno, solo recibe radianes
        double funcioseno = Math.sin(1.5);
        System.out.println("funcioseno = " + funcioseno);

        //función coseno, también recibe el parametro en radianes
        double funsioncoseno = Math.cos(0.00);
        System.out.println("funsioncoseno = " + funsioncoseno);


    }
}
